package jdepend.util.refactor;

import jdepend.framework.exception.JDependException;

public class RefactorException extends JDependException {

	public RefactorException() {
		super();
	}

	public RefactorException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public RefactorException(String arg0) {
		super(arg0);
	}

	public RefactorException(Throwable arg0) {
		super(arg0);
	}

}

package jdepend.util.todolist;

import jdepend.framework.exception.JDependException;

public class TODOListException extends JDependException {

	public TODOListException() {
		super();
	}

	public TODOListException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public TODOListException(String arg0) {
		super(arg0);
	}

	public TODOListException(Throwable arg0) {
		super(arg0);
	}

}

package jdepend.framework.exception;

public class JDependException extends Exception {

	public JDependException() {
		super();
	}

	public JDependException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public JDependException(String arg0) {
		super(arg0);
	}

	public JDependException(Throwable arg0) {
		super(arg0);
	}

}

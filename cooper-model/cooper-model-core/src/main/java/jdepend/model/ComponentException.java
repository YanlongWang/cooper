package jdepend.model;

import jdepend.framework.exception.JDependException;

public class ComponentException extends JDependException {

	public ComponentException() {
		super();
	}

	public ComponentException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ComponentException(String arg0) {
		super(arg0);
	}

	public ComponentException(Throwable arg0) {
		super(arg0);
	}

}

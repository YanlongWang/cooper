package jdepend.model.component.modelconf;

import jdepend.framework.exception.JDependException;

public class ComponentConfException extends JDependException {

	public ComponentConfException() {
		super();
	}

	public ComponentConfException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ComponentConfException(String arg0) {
		super(arg0);
	}

	public ComponentConfException(Throwable arg0) {
		super(arg0);
	}

}

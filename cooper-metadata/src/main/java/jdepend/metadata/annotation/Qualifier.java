package jdepend.metadata.annotation;

public class Qualifier implements Annotation {

	private static final long serialVersionUID = -2441646634122188475L;

	private String value;

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	@Override
	public String toString() {
		if (value != null) {
			return "Qualifier [value=" + value + "]";
		} else {
			return "Qualifier";
		}
	}

	public boolean direct(Service service) {

		if (this.value == null) {
			return false;
		}

		if (service == null || service.getValue() == null) {
			return false;
		}

		return this.value.equals(service.getValue());

	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Qualifier other = (Qualifier) obj;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}
}

package jdepend.knowledge.domainanalysis;

import jdepend.framework.exception.JDependException;

public class ExpertException extends JDependException {

	public ExpertException() {
		super();
	}

	public ExpertException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ExpertException(String arg0) {
		super(arg0);
	}

	public ExpertException(Throwable arg0) {
		super(arg0);
	}

}
